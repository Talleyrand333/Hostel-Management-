    private void jLabel7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel7MouseClicked
        Admin_interface ai =new Admin_interface();
        sql="SELECT username from admin_login_details where login_time=?";
        try {
            ps=con.prepareStatement(sql);
            ps.setString(1,this.login_time);
            rs=ps.executeQuery();
            if(rs.next()){
                String user_=rs.getString("username");
                ai.show_user.setText(user_);
                String query1="SELECT photo from admin_details where username=?";
                PreparedStatement ps1=con.prepareStatement(query1);
                ps1.setString(1,user_);
                ResultSet rs1=ps1.executeQuery();
                if(rs1.next()){
                    byte[] photo_=rs1.getBytes("photo");
                    ImageIcon ic1=new ImageIcon(photo_);
                    Image ic2=ic1.getImage().getScaledInstance(ai.pic_holder.getWidth(),ai.pic_holder.getHeight(),Image.SCALE_SMOOTH);
                    ai.pic_holder.setIcon(new ImageIcon(ic2));
                    ai.login_time=this.login_time;
                }
                        
               }   
        } catch (SQLException ex) {
            Logger.getLogger(Student_Registration.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.dispose();
        ai.setVisible(true);
    }//GEN-LAST:event_jLabel7MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       if(this.regno1.getText().equals("")){
           JOptionPane.showMessageDialog(null,"Enter a registration number");
       }
       else{
           try {
               this.gender.removeAllItems();
               this.hostel_applied.removeAllItems();
               this.hostel_applied.addItem("Select");
               this.room_applied.removeAllItems();
               this.room_applied.addItem("Select");
               sql="select firstname,othernames,dob,email,phone,gender,photo from student_details where regno=?";
               ps=con.prepareStatement(sql);
               ps.setString(1,this.regno1.getText());
               rs=ps.executeQuery();
               if(rs.next()){
               String fn=rs.getString("firstname");
               this.firstname.setText(fn);
               String ln=rs.getString("othernames");
               this.othername.setText(ln);
               String dob_=rs.getString("dob");
               this.dob.setText(dob_);
               String em_=rs.getString("email");
               this.email.setText(em_);
               String ph_=rs.getString("phone");
               this.phone.setText(ph_);
               String gen_=rs.getString("gender");
               if(gen_.equals("M")){
                   this.gender.addItem("Male");
               }
               else{
                   this.gender.addItem("Female");
                           }
                byte[] dbpass=rs.getBytes("photo");
                ImageIcon icn=new ImageIcon(dbpass);
                Image img1=icn.getImage();
                Image img2=img1.getScaledInstance(p_holder.getWidth(),p_holder.getHeight(),Image.SCALE_SMOOTH);
                ImageIcon icn2=new ImageIcon(img2);
                p_holder.setIcon(icn2);
                String sql2="Select hostel_name from hostels where gender=?";
                ps=con.prepareStatement(sql2);
                ps.setString(1,gen_);
                rs=ps.executeQuery();
                while(rs.next()){
                ArrayList<String> arr=new ArrayList();
                arr.add(rs.getString("hostel_name"));
                for(String i:arr){
                this.hostel_applied.addItem(i);
                }
                }
                Hostel_class hc=new Hostel_class(this.hostel_applied.getSelectedItem().toString());
                    for(Room_class rc:hc.hostel){
                        this.room_applied.addItem(rc.getRoomName());
                    }
               }
               else{
                   JOptionPane.showMessageDialog(this,"No Details Found!");
               }
           } catch (SQLException ex) {
               Logger.getLogger(Student_Application.class.getName()).log(Level.SEVERE, null, ex);
           }
            
                 
                }
    }//GEN-LAST:event_jButton1ActionPerformed

